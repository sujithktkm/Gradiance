    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        try
        {
            BeginDate = formatter.parse(startDateText.getText());
            EndDate = formatter.parse(endDateText.getText());
            BeginSql = new java.sql.Date(BeginDate.getTime());
            EndSql = new java.sql.Date(EndDate.getTime());
            String sql ="{call UPDATE_HW(?,?,?,?,?,?,?,?)}";
            Connection conn =Gradiance.conn ;
            CallableStatement callableStatement = conn.prepareCall(sql);
            callableStatement.setInt(1, hw_id);
            callableStatement.setDate(2, BeginSql);
            callableStatement.setDate(3,EndSql);
            callableStatement.setInt(4,Integer.parseInt(numberofAttemptsText.getText()));
            callableStatement.setInt(5,Integer.parseInt(correctAnswerPointsText.getText()));
            callableStatement.setInt(6,Integer.parseInt(inCorrectAnswerPointsText.getText()));
            callableStatement.setString(7,scoreSelectionCmbBox.getSelectedItem().toString());
            callableStatement.setInt(8,Integer.parseInt(difficultylevelText.getText()));
            //callableStatement.registerOutParameter(7,java.sql.Types.INTEGER);
            callableStatement.execute();
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
        finally
        {
            this.setVisible(false);
            globalParent.globalProfessor_CourseHomePage.setVisible(true);
        }
    }//GEN-LAST:event_editButtonActionPerformed

