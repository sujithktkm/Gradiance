    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try
        {
            String sql ="{call EDIT_HW(?,?)}";
            Connection conn =Gradiance.conn ;
            CallableStatement callableStatement = conn.prepareCall(sql);
            callableStatement.setInt(1, hw_id);
            callableStatement.registerOutParameter(2,OracleTypes.CURSOR);
            callableStatement.execute();
            ResultSet result = ((OracleCallableStatement)callableStatement).getCursor(2);
            
            callableStatement.execute();
            while(result.next())
            {
                startDateText.setText(result.getDate(1).toString());
                endDateText.setText(result.getDate(2).toString());
                numberofAttemptsText.setText(new Integer(result.getInt(3)).toString());
                correctAnswerPointsText.setText(new Integer(result.getInt(4)).toString());
                inCorrectAnswerPointsText.setText(new Integer(result.getInt(5)).toString());
                scoreSelectionCmbBox.addItem(result.getString(6));
                difficultylevelText.setText(new Integer(result.getInt(7)).toString());
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

